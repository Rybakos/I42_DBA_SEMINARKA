            <h2>📍 Klíčové prvky kultury</h2>
            <ul>
                <li>🛕 <b>Buddhismus:</b> Hlavní náboženství ovlivňující každodenní život.</li>
                <li>🙏 <b>Respekt:</b> Wai (spojené ruce a úklon) je tradiční pozdrav.</li>
                <li>🎉 <b>Festivaly:</b> Songkran (vodní festival), Loi Krathong (festival světel).</li>
                <li>🍛 <b>Kuchyně:</b> Vyvážené chutě – sladké, kyselé, pálivé a slané.</li>
                <li>🎭 <b>Umění:</b> Tradiční thajské tance a loutkové divadlo.</li>
            </ul>
